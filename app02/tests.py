def md5(input_string):
    m = hashlib.md5(settings.SECRET_KEY.encode('utf-8'))

    # 更新md5对象内容，可以多次update，这里一次性完成
    m.update(input_string.encode('utf-8'))

    # 获取16进制格式的MD5值
    return m.hexdigest()


